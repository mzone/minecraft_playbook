/** ラベルからItemInfoを検索（前方一致） */
export function findItem(label: string): ItemInfo | null {
  // 完全一致を優先
  for (const [key, info] of ITEM_MAP) {
    if (label === key) return info;
  }
  // 前方一致
  for (const [key, info] of ITEM_MAP) {
    if (label.startsWith(key)) return info;
  }
  return null;
}
